select
  TRY_CAST(ts_riferimento AS TIMESTAMP_NTZ) as ts_riferimento,
  TRY_CAST('{{ run_started_at }}' AS TIMESTAMP_NTZ) as ts_caricamento,
  TRY_CAST(CAMPAIGNID AS VARCHAR(30)) AS campaignid,
  TRY_CAST(MARKETINGPLANID AS VARCHAR(30)) AS marketingplanid,
  TRY_CAST(PROGRAMID AS VARCHAR(30)) AS programid,
  TRY_CAST(NOMEPIANOMARKETING AS VARCHAR(80)) AS nomepianomarketing,
  TRY_CAST(DATAINIZIOPIANOMARKETING AS VARCHAR(20)) AS datainiziopianomarketing,
  TRY_CAST(DATAFINEPIANOMARKETING AS VARCHAR(20)) AS datafinepianomarketing,
  TRY_CAST(NOMEPROGRAMMA AS VARCHAR(80)) AS nomeprogramma,
  TRY_CAST(DATAINIZIOPROGRAMMA AS VARCHAR(20)) AS datainizioprogramma,
  TRY_CAST(DATAFINEPROGRAMMA AS VARCHAR(20)) AS datafineprogramma,
  TRY_CAST(NOMECAMPAGNA AS VARCHAR(80)) AS nomecampagna,
  TRY_CAST(DATAINIZIOCAMPAGNA AS VARCHAR(20)) AS datainiziocampagna,
  TRY_CAST(DATAFINECAMPAGNA AS VARCHAR(20)) AS datafinecampagna,
  TRY_CAST(CANALECAMPAGNA AS VARCHAR(80)) AS canalecampagna,
  TRY_CAST(NATURACAMPAGNA AS VARCHAR(80)) AS naturacampagna,
  TRY_CAST(STATOCAMPAGNA AS VARCHAR(80)) AS statocampagna,
  TRY_CAST(CATEGORIACOMUNICAZIONE AS VARCHAR(80)) AS categoriacomunicazione,
  TRY_CAST(OBIETTIVOBUSINESS AS VARCHAR(80)) AS obiettivobusiness,
  TRY_CAST(CLUSTERCAMPAGNA AS VARCHAR(80)) AS clustercampagna
from {{ source('source_l0','campaign') }}
