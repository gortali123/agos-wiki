In CTE_BASE la conversione in TO_DATE risulta con errore in quanto in campo SVCRSC_MESE_ESTRAZIONE è presente un valore non idoneo
2017-[13]-01. Al momento per la eseguibilità delle CTE utilizzato un CASE WHEN per superare log di errore bloccante.

