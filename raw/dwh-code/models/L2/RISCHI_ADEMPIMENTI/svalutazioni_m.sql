LEFT JOIN {{ ref('svesfcrsc') }} PREV
    ON  CURR.SVCRSC_PRATICA = PREV.SVCRSC_PRATICA
    AND CURR.SVCRSC_PROCEDURA = PREV.SVCRSC_PROCEDURA
    AND PREV.SVCRSC_ANNO_ESTRAZIONE = (CURR.SVCRSC_ANNO_ESTRAZIONE - 1)
    AND PREV.SVCRSC_MESE_ESTRAZIONE = 12 
LEFT JOIN {{ ref('legame_cartolarizzazioni') }} B
    ON  CURR.SVCRSC_PRATICA = B.CD_PRATICA
    AND CURR.SVCRSC_PROCEDURA = B.TP_PROCEDURA
    AND CURR.SVCRSC_ANNO_ESTRAZIONE = EXTRACT(YEAR FROM B.DT_OSSERVAZIONE)
    AND CURR.SVCRSC_MESE_ESTRAZIONE = EXTRACT(MONTH FROM B.DT_OSSERVAZIONE)
LEFT JOIN {{ ref('svesfcrsc') }} PREC_BASE
    ON  CURR.SVCRSC_PRATICA = PREC_BASE.SVCRSC_PRATICA
    AND CURR.SVCRSC_PROCEDURA = PREC_BASE.SVCRSC_PROCEDURA
    AND CURR.SVCRSC_ANNO_ESTRAZIONE = PREC_BASE.SVCRSC_ANNO_ESTRAZIONE
    AND PREC_BASE.SVCRSC_MESE_ESTRAZIONE = CURR.SVCRSC_MESE_ESTRAZIONE - 1
LEFT JOIN {{ ref('svesfcrsc') }} CART
    ON  B.CD_PRATICA_CARTOLARIZZATA = CART.SVCRSC_PRATICA
    AND CURR.SVCRSC_PROCEDURA = CART.SVCRSC_PROCEDURA
    AND CURR.SVCRSC_ANNO_ESTRAZIONE = CART.SVCRSC_ANNO_ESTRAZIONE
    AND CURR.SVCRSC_MESE_ESTRAZIONE = CART.SVCRSC_MESE_ESTRAZIONE
LEFT JOIN {{ ref('svesfcrsc') }} PREC_CART
    ON  B.CD_PRATICA_CARTOLARIZZATA = PREC_CART.SVCRSC_PRATICA
    AND CURR.SVCRSC_PROCEDURA = PREC_CART.SVCRSC_PROCEDURA
    AND CURR.SVCRSC_ANNO_ESTRAZIONE = PREC_CART.SVCRSC_ANNO_ESTRAZIONE
    AND PREC_CART.SVCRSC_MESE_ESTRAZIONE = CURR.SVCRSC_MESE_ESTRAZIONE - 1
