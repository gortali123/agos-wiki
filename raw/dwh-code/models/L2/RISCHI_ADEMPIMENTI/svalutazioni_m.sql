            ELSE
            LAST_DAY(
            TO_DATE(CURR.SVCRSC_ANNO_ESTRAZIONE || '-' || CURR.SVCRSC_MESE_ESTRAZIONE || '-01', 'YYYY-MM-DD')
            )
        END AS DT_OSSERVAZIONE,
    CASE
        WHEN NVL(PREV.SVCRSC_FONDO_TOTALE, 0) <> 0 AND NVL(CURR.SVCRSC_FONDO_TOTALE, 0) <> 0
             THEN 'UGU'
        WHEN NVL(PREV.SVCRSC_FONDO_TOTALE, 0) = 0  AND NVL(CURR.SVCRSC_FONDO_TOTALE, 0) <> 0
             THEN 'ENT'
        WHEN NVL(PREV.SVCRSC_FONDO_TOTALE, 0) <> 0 AND NVL(CURR.SVCRSC_FONDO_TOTALE, 0) = 0
             THEN 'USC'
        ELSE 'ND'
    END AS CD_STATO_SVALZ,
