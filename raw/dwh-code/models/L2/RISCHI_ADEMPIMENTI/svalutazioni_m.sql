            ELSE NVL(CART.SVCRSC_FSC_RIPRESA_INCASSO, 0) - NVL(PREC_CART.SVCRSC_FSC_RIPRESA_INCASSO, 0)
        END
    )', precision=11) }} AS EU_UTILE_CESS,
