-- Entita' L2: ACCETTAZIONE_INPUT  |  Subject Area: SCORING  |  Storicizzazione: S2
-- Sorgente: landing XML CDE (payload StrategyOneRequest, ProcessCode = 'TARGATO')
-- Grana: per Applicant (PK = CD_INQUIRYCODE + CD_RUOLO)
-- Livelli sorgente nel payload:
--   * Header                         -> CD_INQUIRYCODE, CD_PROCESSCODE
--   * Body/Richiesta/Variables       -> campi livello pratica (REQ)
--   * Categories/APPLICANT/Variables -> campi livello soggetto (APP), incl. RUOLO
--   * APPLICANT ha una PROPRIA Categories con nodi ripetuti (SEGNALAZIONI_ANAGRAFICHE, ...)
--   * Categories/CATENA_AGGIUNTIVA   -> campi "aggiuntiva" (nodo ripetuto a livello pratica)
-- Nodi ripetuti (CATENA_AGGIUNTIVA a livello pratica, SEGNALAZIONI_ANAGRAFICHE a livello
--   Applicant): PRE-AGGREGATI con LISTAGG(DISTINCT) in CTE dedicate raggruppate per chiave,
--   poi LEFT JOIN sulla grana finale. (Le subquery correlate in SELECT non sono supportate
--   da Snowflake: 'Unsupported subquery type cannot be evaluated'.)
--   NOTA: CATENA_AGGIUNTIVA pende dalla Richiesta, quindi il valore aggregato per InquiryCode
--   si ripete su tutti gli Applicant della pratica. SEGNALAZIONI_ANAGRAFICHE pende
--   dall'APPLICANT, quindi e' aggregato per (InquiryCode, RUOLO).
-- NOTA storicizzazione: TS_INSERIMENTO valorizzato da TS_RIFERIMENTO della L1 CDE (non
--   LASTMODIFIEDDATA / CURRENT_TIMESTAMP), incrementale append filtrato su TS_RIFERIMENTO.
-- ============================================================================

WITH src AS (

    SELECT
        TS_RIFERIMENTO,
        VALUE AS xml_doc
    FROM {{ ref('cde') }}          -- ASSUNZIONE: modello landing CDE = ref('cde'); VALIDARE nome reale
    WHERE XMLGET(XMLGET(XMLGET(VALUE, 'StrategyOneRequest'), 'Header'), 'ProcessCode'):"$"::VARCHAR = 'TARGATO'
    {% if is_incremental() %}
      AND TS_RIFERIMENTO > (SELECT COALESCE(MAX(TS_INSERIMENTO), '1900-01-01'::TIMESTAMP_NTZ) FROM {{ this }})
    {% endif %}

),

nodes AS (

    SELECT
        TS_RIFERIMENTO,
        xml_doc,
        XMLGET(XMLGET(XMLGET(xml_doc, 'StrategyOneRequest'), 'Header'), 'InquiryCode'):"$"::VARCHAR AS CD_INQUIRYCODE,
        XMLGET(XMLGET(xml_doc, 'StrategyOneRequest'), 'Header')                                          AS n_header,
        XMLGET(XMLGET(XMLGET(XMLGET(xml_doc, 'StrategyOneRequest'), 'Body'), 'Richiesta'), 'Variables')  AS n_req_var,
        XMLGET(XMLGET(XMLGET(XMLGET(xml_doc, 'StrategyOneRequest'), 'Body'), 'Richiesta'), 'Categories') AS n_categories
    FROM src

),

-- Espansione APPLICANT (grana finale per soggetto)
applicant AS (

    SELECT
        n.TS_RIFERIMENTO,
        n.CD_INQUIRYCODE,
        n.n_header,
        n.n_req_var,
        XMLGET(F.value, 'Variables')  AS n_app_var,
        XMLGET(F.value, 'Categories') AS n_app_categories
    FROM nodes n,
    {{ flatten_xml('n_categories', 'APPLICANT', 'F') }}

),

-- Pre-aggregazione dei nodi ripetuti CATENA_AGGIUNTIVA (livello pratica -> per InquiryCode)
catena_agg AS (

    SELECT
        n.CD_INQUIRYCODE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'RETE_VENDITA_AGGIUNTIVA'):"$"::VARCHAR, ',') AS CD_RETE_VENDITA,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'AGENTE_AGGIUNTIVA'):"$"::VARCHAR, ',') AS CD_AGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'MacroAreaAgente'):"$"::VARCHAR, ',') AS DS_MACROAREA_AGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'StatoAgente'):"$"::VARCHAR, ',') AS CD_STATO_AGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'AttributoAgente'):"$"::VARCHAR, ',') AS CD_ATTRIBUTO_AGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'TipologiaAgente'):"$"::VARCHAR, ',') AS TP_AGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'SUBAGENTE_AGGIUNTIVA'):"$"::VARCHAR, ',') AS CD_SUBAGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'MacroAreaSubAgente'):"$"::VARCHAR, ',') AS DS_MACROAREA_SUBAGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'StatoSubAgente'):"$"::VARCHAR, ',') AS CD_STATO_SUBAGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'AttributoSubAgente'):"$"::VARCHAR, ',') AS CD_ATTRIBUTO_SUBAGENTE,
        LISTAGG(DISTINCT XMLGET(XMLGET(cf.value, 'Variables'), 'TipologiaSubAgente'):"$"::VARCHAR, ',') AS TP_SUBAGENTE
    FROM nodes n,
    LATERAL FLATTEN(input => n.n_categories:"$", OUTER => TRUE) cf
    WHERE cf.value:"@"::VARCHAR = 'CATENA_AGGIUNTIVA'
    GROUP BY n.CD_INQUIRYCODE

),

-- Pre-aggregazione dei nodi ripetuti SEGNALAZIONI_ANAGRAFICHE (livello Applicant)
-- Chiave = (InquiryCode, RUOLO): il RUOLO si legge dal Variables dell'APPLICANT che contiene il nodo.
