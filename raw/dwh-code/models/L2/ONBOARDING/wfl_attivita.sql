        T.WFISAT_ATTIVITA_PREC_3 AS CD_ATTIVITA_PREC_3,
        T.WFISAT_ATTIVITA_PREC_4 AS CD_ATTIVITA_PREC_4,
        T.WFISAT_ATTIVITA_PREC_5 AS CD_ATTIVITA_PREC_5,
        T.WFISAT_TIPO_CHIUSURA AS TP_CHIUSURA,
        T.WFISAT_DATA_BO AS CD_DATA_BO,
        T.WFISAT_PROG_RICICLO AS CD_PROG_RICICLO,
        T.WFISAT_PRG_RET AS CD_PRG_RET,
        T.WFISAT_LIQ_SC AS CD_LIQ_SC,
        {{ custom_to_date ('T.WFISAT_DATA_COM_SIN') }} AS DT_COM_SIN,
        T.WFISAT_CODICE_SIN AS CD_CODICE_SIN,
        T.WFISAT_CLASS_SIN AS CD_CLASS_SIN,
        T.WFISAT_TIPO_SIN AS TP_SIN,
        T.WFISAT_STATO_SIN AS CD_STATO_SIN,
        T.WFISAT_ASS_SIN AS CD_ASS_SIN,
        T.WFISAT_STATO_PSV AS CD_STATO_PSV,
        T.WFISAT_BLOCCO_PSV AS CD_BLOCCO_PSV,
        T.WFISAT_PRG_PSV_EXT AS PR_PSV_EXT,
        T.WFISAT_NOTE AS DS_NOTE,
        T.LASTMODIFIEDDATA AS LASTMODIFIEDDATA
    FROM {{ ref('wfisfat') }} T
    WHERE T.FL_DELETED = 'N'
),

