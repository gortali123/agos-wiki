        {{ custom_to_date ('T.WFISAT_DATA_SCADENZA') }} AS DT_SCADENZA_ATTIVITA,
        {{ custom_to_timestamp_ntz('T.WFISAT_DATA_CHIUSURA', 'T.WFISAT_ORA_CHIUSURA') }} AS TS_CHIUSURA_ATTIVITA,
        T.WFISAT_UTENTE_CHIUSURA AS CD_UTENTE_CHIUSURA_ATTIVITA,
        T.WFISAT_PRG_SIN AS CD_PRG_SIN,
        T.WFISAT_PRG_PSV AS CD_PRG_PSV,
