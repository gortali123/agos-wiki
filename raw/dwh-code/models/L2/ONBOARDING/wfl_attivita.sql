            'TS_CHIUSURA_ATTIVITA',
            'CD_UTENTE_CHIUSURA_ATTIVITA',
            'CD_PRG_SIN',
            'CD_PRG_PSV',
            'CD_AZIONE_PSV',
            'CD_ATTIVITA_PREC',
            'CD_ATTIVITA_PREC_2',
            'CD_ATTIVITA_PREC_3',
